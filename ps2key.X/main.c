/* 初期化処理 */
void initTsk(void)
{
    sciBuf = 0x00;                      //バッファ初期化
    bitCnt = 0;                         //入力ビット数リセット
    tableSW = 0;                        //小文字テーブル
    putByte(0xff);
}

/* SDAのデータを保存する */
void scanBit(void)
{
    if(bitCnt <= 8)
    {   //8ビット変数があふれないなら
        sciBuf = (uint8_t)(sciBuf << 1);           //ビットシフト
        if(IO_RB1_GetValue())
        {   //入力ピンがhighなら
            sciBuf |= 0x01;             //ビットセット
        }
        else
        {   //入力ピンがLowなら
            sciBuf &= 0xfe;             //ビットリセット
        }
    }
    
    bitCnt++;
}

uint8_t trans(void)
{
    uint8_t ret = 0x00;                 //戻り値
    uint8_t ch = sciBuf;                //入力生データ
    
    if(ch == SHIFT_CODE)
    {   //SHIFTが押されたら
        tableSW = 1;                    //大文字テーブルに切り替え
    }
    else if(ch == CTRL_CODE)
    {   //CTRLが押されたら
        tableSW = 2;                    //特殊テーブルに切り替え
    }
    else if(ch == OFF_CODE)
    {   //キーが解放されたら
        tableSW = 0;                    //小文字テーブルに切り替え
    }
    else
    {   //普通のキーが押下されたら
        ret = table[tableSW][ch];
    }
    
    return ret;
}

/* 8ビット出力 */
void putByte(uint8_t inC)
{   //RA1,RA0,RA7,RA6,RB7,RB6,RB5.RB4
    RA1 = (inC & 0x80) ? 1 : 0;
    RA0 = (inC & 0x40) ? 1 : 0;
    RA7 = (inC & 0x20) ? 1 : 0;
    RA6 = (inC & 0x10) ? 1 : 0;
    RB7 = (inC & 0x08) ? 1 : 0;
    RB6 = (inC & 0x04) ? 1 : 0;
    RB5 = (inC & 0x02) ? 1 : 0;
    RB4 = (inC & 0x01) ? 1 : 0;
}

/* メイン処理 */
void mainTsk(void)
{
    if( timint == TIME_UP ){
        timint = TIME_OFF;
        putByte(trans());               //8ビット出力
        sciBuf = 0;                     //受信バッファクリア
        bitCnt = 0;
    }
}

/* EXT_INTの割り込みコールバック */
void Int_CallBack(void)
{   //RB0ピンの立下りで呼ばれるコールバック
    timint = INT_TIMEOUT;               //タイマ10ms設定
    scanBit();                          //1ビット入力 
}

/* タイマ管理 */
void mainTimer(void)
{    
    if( timint > TIME_UP )
    {   //1msごとにカウントダウン
        timint--;
    }
}
